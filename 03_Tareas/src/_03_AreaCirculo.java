
public class _03_AreaCirculo {

	public static void main(String[] args) {
		
		final double pi = 3.1416;
		double radio = 0;
		double area = 0;
		
		radio = 10;
		radio = radio * radio;
		area = pi * radio;
		
		System.out.println(area);
		
		radio = 20;
		radio = radio * radio;
		area = pi * radio;
		
		System.out.println(area);
	}

}
